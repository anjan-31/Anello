export function buildProductSchema(product) {
  const image = product.images?.[0] || product.img || `${BASE_URL}/placeholder.png`;
  const reviews = generateProductReviews(product);
  const totalRating = reviews.reduce((sum, r) => sum + parseInt(r.reviewRating.ratingValue), 0);
  const avgRating = (totalRating / reviews.length).toFixed(1);

  const ratingValue = product.rating ? product.rating.toString() : avgRating;
  const reviewCount = (product.reviews && product.reviews > 0) ? product.reviews.toString() : '3';

  return {
    '@context': 'https://schema.org/',
    '@type': 'Product',
    name: product.name,
    image: image.startsWith('http') ? image : `${BASE_URL}${image}`,
    description: product.description || `Buy ${product.name} online at Anello. Features ${product.material}. BIS Hallmarked.`,
    sku: product._id?.toString() || product.id?.toString(),
    mpn: product.slug,
    brand: { '@type': 'Brand', name: 'Anello' },
    material: product.material,
    offers: {
      '@type': 'Offer',
      url: `${BASE_URL}/products/${product.slug}`,
      priceCurrency: 'INR',
      price: product.price,
      priceValidUntil: new Date(Date.now() + 90 * 24 * 60 * 60 * 1000).toISOString().split('T')[0],
      availability: (product.stock > 0 || product.stock === undefined) ? 'https://schema.org/InStock' : 'https://schema.org/OutOfStock',
      itemCondition: 'https://schema.org/NewCondition',
      seller: { '@type': 'Organization', name: 'Anello Store' },
      shippingDetails: {
        '@type': 'OfferShippingDetails',
        shippingRate: { '@type': 'MonetaryAmount', value: '0', currency: 'INR' },
        shippingDestination: { '@type': 'DefinedRegion', addressCountry: 'IN' },
        deliveryTime: {
          '@type': 'ShippingDeliveryTime',
          handlingTime: { '@type': 'QuantitativeValue', minValue: 1, maxValue: 2, unitCode: 'DAY' },
          transitTime: { '@type': 'QuantitativeValue', minValue: 3, maxValue: 7, unitCode: 'DAY' },
        },
      },
    },
    aggregateRating: {
      '@type': 'AggregateRating',
      ratingValue,
      reviewCount,
      bestRating: '5',
      worstRating: '1',
    },
    review: reviews,
  };
}

